package block

import "time"

type GroupInfo struct {
	Time time.Time
}

type GroupInfoInternal struct {
	Unknown0 [36]byte
	Time     [19]byte
	Unknown1 [20]byte
}

func (info GroupInfo) buildInternal() PieceInternal {
	var internal GroupInfoInternal
	internal.Unknown0 = [36]byte{
		0x00, 0x00, 0x00, 0x00, 0xff, 0x00, 0x00, 0x00,
		0x00, 0x02, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
		0x06, 0xaa, 0x00, 0x00, 0x01, 0xe2, 0x00, 0x00,
		0x00, 0x00, 0x32, 0x30, 0x30, 0x38, 0x2d, 0x30,
		0x36, 0x2d, 0x32, 0x33,
	}
	copy(internal.Time[:], info.Time.Format(dtLayout))
	internal.Unknown1 = [20]byte{
		0x2D, 0x83, 0x00, 0x03, 0xC8, 0x7E, 0x00, 0x00,
		0x87, 0x2B, 0x01, 0xF6, 0x02, 0x57, 0x01, 0xF2,
		0x01, 0xD9, 0x01, 0x82,
	}

	return internal
}
